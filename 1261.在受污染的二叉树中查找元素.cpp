/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class FindElements {
  private:
    unordered_set<int> values;

    void reBuild(TreeNode *node, int val) {
        if (node == nullptr)
            return;
        node->val = val;
        values.insert(val);
        reBuild(node->left, val * 2 + 1);
        reBuild(node->right, val * 2 + 2);
    }

  public:
    FindElements(TreeNode *root) { reBuild(root, 0); }

    bool find(int target) { return values.count(target); }
};

